function menuAcoesServidor() {
    if (document.getElementById("acoesServidor").style.display === "none") {
        document.getElementById("acoesServidor").style.display = "block"
    } else if (document.getElementById("acoesServidor").style.display === "block") {
        document.getElementById("acoesServidor").style.display = "none"
    }
}

